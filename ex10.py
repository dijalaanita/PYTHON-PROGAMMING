tabby_cat = "\tI'm tabbed in."
persian = "i'm split\non a line."
backlash = "i'm \\ a \\ cat."

fat = '''
i'll do a list:
\t * cat food
\t * fish
\t * catnip \n\t * grass
'''

print(tabby_cat)
print(persian)
print(backlash)
print(fat)

# \\ -> backlash
# \' -> single quote
# \" -> double quote
# \t -> horizontal tab, bullet points basically
# \v -> vertical tab
